import Foundation

struct Model {
	var name: String
	var text: String
}

extension Model : Hashable {
    var hashValue: Int {
		return "\(name),\(text)".hashValue
	}
}

extension Model: Equatable {}
func ==(lhs: Model, rhs: Model) -> Bool {
	return lhs.hashValue == rhs.hashValue
}

